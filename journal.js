// Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}

// Check if a number is even
function isEven(num) {
  return num % 2 === 0;
}

// Compute square of a number
function square(num) {
  return num * num;
}

// Find larger of two numbers
function max(a, b) {
  return a > b ? a : b;
}

// Convert Celsius to Fahrenheit
function celsiusToFahrenheit(celsius) {
  return (celsius * 9/5) + 32;
}
